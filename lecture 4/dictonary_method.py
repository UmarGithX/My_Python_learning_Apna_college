# to show you the Keys in a Dictonary the function Use "Dict.keys()"
#this function will print the number of keys in our Dictonary.
dict = {
    "name" : "Umar",
    "age" : "fifteen",
    "learning" : "Coding"
}
print(dict)
print(dict.keys())

# dict.len()
# it can print the number of key in a dictonary
print(len(dict))

# we can convert the values in to list and tuple From dictonary
print(list(dict))

# dict.values()
# this function allow us to print the values of the key 
print(dict.values())


